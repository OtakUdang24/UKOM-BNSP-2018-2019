    public Object[][] getDataModel(){
        Object data[][] = null;
        try{
            stat = con.createStatement();
            String sql = "CALL selectAllMeja()";
            rs = stat.executeQuery(sql);
            rs.last();
            int jmlRec = rs.getRow();
            data = new Object[jmlRec][8];
            rs.beforeFirst();
            int i = -1;
            while (rs.next()){
                i++;
                data[i][0] = rs.getString("no");
              
            }
        }catch (SQLException e){
            System.err.println(e.getMessage());
        }
        return data;
    }
   
    public void tampilTabel(){
        String[] judul = new String[]{"No Meja"};
        tabelModel = new DefaultTableModel(getDataModel(), judul);
        meja.setModel(tabelModel);
    }
    
